//default constractor
CFigure::CFigure(){}
///////////////////////////////////////////////////////////////////////////////////////////////////////////////
//non-default constractor
CFigure::CFigure(GfxInfo FigureGfxInfo)
{ 
	FigGfxInfo = FigureGfxInfo;	                //Default status is non-filled.
	Selected = false;                           //state of the new created figure unselected
	ID = counter;                             //set the id for each new created figure
	isHidden = false;                           //
}
///////////////////////////////////////////////////////////////////////////////////////////////////////////////
//state the figure from not selected to selected
void CFigure::SetSelected(bool s)
{	Selected = s; }
///////////////////////////////////////////////////////////////////////////////////////////////////////////////
//check if the figure is selected or not
bool CFigure::IsSelected() const
{	return Selected; }
